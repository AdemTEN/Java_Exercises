package Loops.Patterns;

public class P21_$$$$$$$$$$$$$$$$4 {

	public static void main(String[] args) {

		int alphabet = 65;

		int row = 5;
		int counter = 1;
		for (int i = 1; i <= row; i++) {

			for (int j = 1; j <= i; j++) {
				System.out.print((char) (alphabet + counter-1) + " ");
				counter++;
			}
			System.out.println();

		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
